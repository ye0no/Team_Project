package net.webplate.vo;


public class LikeVO {
	
	 private int like_no;
	 private int f_num;
	 private String mem_id;
	 private int like_state;
	 private String like_sysdate;
	 
	public int getLike_no() {
		return like_no;
	}
	public void setLike_no(int like_no) {
		this.like_no = like_no;
	}
	public int getF_num() {
		return f_num;
	}
	public void setF_num(int f_num) {
		this.f_num = f_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public int getLike_state() {
		return like_state;
	}
	public void setLike_state(int like_state) {
		this.like_state = like_state;
	}
	public String getLike_sysdate() {
		return like_sysdate;
	}
	public void setLike_sysdate(String like_sysdate) {
		this.like_sysdate = like_sysdate;
	}


}
